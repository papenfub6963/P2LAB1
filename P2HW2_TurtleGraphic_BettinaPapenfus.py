import turtle
pokey = turtle.Turtle()
pokey.color('green')
pokey.pensize(5)
pokey.shape('turtle')

pokey.forward(200)
pokey.left(90)
pokey.forward(200)
pokey.right(90)

pokey.backward(200)
pokey.right(90)
pokey.forward(200)
pokey.left(90)
